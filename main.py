dimensions = matrix.shape
ROWS, COLS = dimensions

SIZE = 1580, 1100


def compute_next_generation(grid):
    next_grid = np.zeros((ROWS, COLS), dtype=int)
    for i in range(ROWS):
        for j in range(COLS):
            live_neighbors = np.sum(grid[i - 1:i + 2, j - 1:j + 2]) - grid[i, j]
            if grid[i, j] == 1:
                if live_neighbors < 2 or live_neighbors > 3:
                    next_grid[i, j] = 0
                else:
                    next_grid[i, j] = 1
            else:
                if live_neighbors == 3:
                    next_grid[i, j] = 1
    return next_grid


